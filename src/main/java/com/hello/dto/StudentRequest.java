package com.hello.dto;

public class StudentRequest implements Request{

    private Integer id;

    private String name;

    public void setName(String name) {
        this.name = name;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
